    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        if (ListaFuncionario.getSelectedRow() != -1) {
            cadastro C = new cadastro();
            cadastroDAO dao = new cadastroDAO();
            String B = (String) SelecGenero.getSelectedItem();
            C.setNome(txtNome.getText());
            C.setEndereco(txtEndereco.getText());
            C.setEmail(txtEmail.getText());
            C.setCPF(Integer.parseInt(txtCpf.getText()));
            C.setRG(Integer.parseInt(txtRg.getText()));
            C.setTelefone(Integer.parseInt(txtTelefone.getText()));
            C.setSenha(txtSenha.getText());
            C.setFunge(radioGerente.isEnabled());
            C.setGenero(B);
            C.setId_conta((int)ListaFuncionario.getValueAt(ListaFuncionario.getSelectedRow(), 0));

            txtNome.setText("");
            txtEndereco.setText("");
            txtEmail.setText("");
            txtCpf.setText("");
            txtRg.setText("");
            txtTelefone.setText("");
            txtSenha.setText("");
            radioGerente.setSelected(false);

            dao.update(C);
            readJtable();

        }
    }//GEN-LAST:event_jButton7ActionPerformed

