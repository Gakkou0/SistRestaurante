    public void readJtable(){
    
        DefaultTableModel modelo = (DefaultTableModel) ListaFuncionario.getModel();
        modelo.setNumRows(0);
        cadastroDAO cdao = new cadastroDAO();
        
        for (cadastro c: cdao.read()){
            
            modelo.addRow(new Object[]{
            c.getId_conta(),
            c.getNome(),
            c.getEndereco(),
            c.getEmail(),
            c.getTelefone(),
            c.getCPF(),
            c.getRG(),
            c.getGenero(),
            c.isFunge()
            
            });
        
        }
        
        
    }
