    private void ConfirmCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmCadastroActionPerformed
        if(txtEndereco.getText().trim().equals("")||txtCpf.getText().trim().equals("") ||txtNome.getText().trim().equals("")||txtRg.getText().trim().equals("")||txtTelefone.getText().trim().equals("")||txtEmail.getText().trim().equals("")||txtSenha.getText().trim().equals("")){
            JOptionPane.showMessageDialog (null,"Preencha todos os campos.");
        }else{int i = JOptionPane.showConfirmDialog(null ,"Tem certeza que deseja salvar?", "Confirmação",JOptionPane.YES_NO_OPTION);
            if (i == JOptionPane.YES_OPTION){
                cadastro C = new cadastro();
                cadastroDAO dao = new cadastroDAO();
                String B = (String) SelecGenero.getSelectedItem();
                C.setNome(txtNome.getText());
                C.setEndereco(txtEndereco.getText());
                C.setEmail(txtEmail.getText());
                C.setCPF(Integer.parseInt(txtCpf.getText()));
                C.setRG(Integer.parseInt(txtRg.getText()));
                C.setTelefone(Integer.parseInt(txtTelefone.getText()));
                C.setSenha(txtSenha.getText());
                C.setFunge(radioGerente.isSelected());
                C.setGenero(B);

                txtNome.setText("");
                txtEndereco.setText("");
                txtEmail.setText("");
                txtCpf.setText("");
                txtRg.setText("");
                txtTelefone.setText("");
                txtSenha.setText("");
                radioGerente.setSelected(false);

                dao.create(C);
                readJtable();

                JOptionPane.showMessageDialog (null, "Cadastro realizado com sucesso!");
            }else {
                repaint();
            }
        }
    }//GEN-LAST:event_ConfirmCadastroActionPerformed

